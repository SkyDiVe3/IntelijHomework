package com.step.calculateSupraincarcat;

public class Calculator {
    public double Adunare(int x, double y){
        double raspuns;
        raspuns = x + y;
        return raspuns;
    }
    public double Scadere(int x, double y){
        double raspuns;
        raspuns = x - y;
        return raspuns;
    }
    public double Inmultire(int x, double y){
        double raspuns;
        raspuns = x * y;
        return raspuns;
    }
    public double Impartire(int x, double y){
        double raspuns;
        raspuns = x / y;
        return raspuns;
    }
}
